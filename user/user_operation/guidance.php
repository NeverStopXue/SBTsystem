<h1>User Guide of SBTSYS</h1>

<p>Hello! Welcome to Second-hand Book Trading System! This is a brief user guide.</p>
<p>On SBTSYS, you can choose your operation on the left panel. <strong>As the start of using SBTSYS, please click "My Information" and set your basic information, if you log in with Jaccount.</strong> </p>
<p>Then, you may post your advertisement on the bulletin! Just click "Upload" and fill the required blanks. After posting the book information, you can check an update by "On sale" bulletin or "My books" bulletin. Before posting, we encourage you to attach a picture of the book for others' reference. By the way, if you are so kind that you would like to donate your books for free, just fill "0" in "Price".</p>
<p>For the ads you have posted, you may edit them in "My books". Every time you sell a book, please subtract one from the quantity of the book. If you have sold out all of your storage, please mark the status as "sold", and it will be removed from the "On-sale" bulletin.</p>
<p>When checking the details of each item on "On sale" bulletin, you may send messages to the seller. You may also check the messages sent to or by you in "My Messages".</p>
<p>Sometimes the administrators may post announcements. Please keep an eye on important affairs in "Announcements".</p>
<p>Due to security issues, we don't implement online payment functions. </p>
<p>Feel free to contact us if you have more questions regarding SBTSYS. We wish you a good business!</p>
